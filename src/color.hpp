typedef struct {
    int r;
    int g;
    int b;
} pixel;

typedef struct {
    std::string name;
    pixel color;
} color;

inline std::string toHex(int n)
{
    std::string hex = "0123456789ABCDEF";
    std::string ret = "";
    while (n) {
        ret.push_back(hex.find(n/16));
        n/=16;
    }
    return ret;
}

inline float redMean(pixel p1, pixel p2) 
{
    int red = (p1.r + p2.r) / 2;
    float delta = (2 + (red / 256.0)) * pow((p2.r-p1.r), 2.0) 
            + 4 * pow((p2.g-p1.g), 2.0) 
            + (2 + ((255-red)/256.0)) * pow((p2.b-p1.b), 2.0);
    return delta;
}

inline float euclidMean(pixel p1, pixel p2) 
{
    return pow((p2.r-p1.r), 2.0) 
           + pow((p2.g-p1.g), 2.0) 
           + pow((p2.b-p1.b), 2.0);
}

